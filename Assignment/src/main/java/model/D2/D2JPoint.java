package model.D2;

import java.awt.*;

public class D2JPoint {
    private double x;
    private double y;

    public D2JPoint() {
    }

    public D2JPoint(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
